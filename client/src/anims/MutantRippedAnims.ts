export const createMutantRippedAnims = (anims: Phaser.Animations.AnimationManager) => {
  const atlasKey = 'mutant_ripped'

  for (const def of mutantRippedAnimDefs) {
    if (anims.exists(def.key)) continue

    anims.create({
      key: def.key,
      frames: anims.generateFrameNames(atlasKey, {
        start: def.start,
        end: def.end,
        prefix: def.prefix,
      }),
      repeat: def.repeat,
      frameRate: def.frameRate,
    })
  }
}
